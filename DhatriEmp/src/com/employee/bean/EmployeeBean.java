package com.employee.bean;

public class EmployeeBean
{
	private String empid;
	private String empname;
	private double empsal;
	private String emploc;
	private double empexp;
	private double emploan;
	private double empemi;
	private String empemail;
	public String getEmpid() {
		return empid;
	}
	public void setEmpid(String empid) {
		this.empid = empid;
	}
	public String getEmpname() {
		return empname;
	}
	public void setEmpname(String empname) {
		this.empname = empname;
	}
	public double getEmpsal() {
		return empsal;
	}
	public void setEmpsal(double empsal) {
		this.empsal = empsal;
	}
	public double getEmpexp() {
		return empexp;
	}
	public void setEmpexp(double empexp) {
		this.empexp = empexp;
	}
	public String getEmploc() {
		return emploc;
	}
	public void setEmploc(String emploc) {
		this.emploc = emploc;
	}
	public double getEmploan() {
		return emploan;
	}
	public void setEmploan(double emploan) {
		this.emploan = emploan;
	}
	public double getEmpemi() {
		return empemi;
	}
	public void setEmpemi(double empemi) {
		this.empemi = empemi;
	}
	
	public String getEmpemail() {
		return empemail;
	}
	public void setEmpemail(String empemail) {
		this.empemail = empemail;
	}

}
